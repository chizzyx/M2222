func fibonacci(int: Int) -> Int {
    print("Calculating fibonacci(\(int))...")
    if(int <= 2){
        print("Terminal case, returning 1.")
        return 1 // Rreturns 1, and no calculation is needed. Both 2 and 1 have the same squence.
    }
    else{
        let outcome =  fibonacci(int: int-1) + fibonacci(int: int-2) // Uses Recursion to calculate the fibonacci sequence.
        print("Non-terminal case, returning \(outcome).")
        return outcome //returns the sequence
    }
}
print("The final result is: \(fibonacci(int: n)).") // Prints the sequence.
