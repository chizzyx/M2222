func fibonacci(int: Int) -> Int {
    print("Calculating fibonacci(\(int))...")
    if(int <= 2){
        print("Terminal case, returning 1.")
        return 1
    }
    else{
        let outcome =  fibonacci(int: int-1) + fibonacci(int: int-2)
        print("Non-terminal case, returning \(outcome).")
        return outcome
    }
}
print("The final result is: \(fibonacci(int: n)).")
